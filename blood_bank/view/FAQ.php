                <h3> Who can donate blood? </h3>
                  <p>Most people who are in good health, weigh at least 110 pounds, and are aged 17 or older can donate blood. However, specific eligibility criteria might vary by country or donation center.</p>           

                     <h3>How often can I donate blood?</h3>
                    <p>Blood donation intervals can differ, but typically, whole blood donation can be done every 56 days. Donating other blood components like platelets or plasma might have different time intervals.  </p>

                    <h3>Will donating blood make me feel weak or tired?</h3>
                        <p>
                         It’s common to feel a slight lightheadedness or tiredness after donating, but this typically passes quickly. Drinking fluids and having a snack after donating can help. </p>
                        
                    <h3>Are there any side effects of blood donation?</h3>
                    <p> Most people do not experience any side effects. However, some might feel lightheaded or dizzy, have a bruise at the donation site, or, in rare cases, have an allergic reaction. </p>

                    <h3>Why is blood donation important?</h3>
                    <p> Blood donation saves lives. It’s used in various medical procedures, for accident victims, during surgeries, and for those with certain medical conditions like anemia. </p>
                    <h3>Can I get infected by donating blood?</h3>
                    <p> 
                    No, blood donation equipment is sterile and used only once, ensuring there is no risk of getting infected by donating blood.</p>

